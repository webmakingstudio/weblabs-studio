const legalTextNodes = document.querySelectorAll("[data-i18n]");
const legalHtmlNodes = document.querySelectorAll("[data-i18n-html]");
const legalAriaNodes = document.querySelectorAll("[data-i18n-aria-label]");
const legalLanguageButtons = document.querySelectorAll("[data-language]");
const legalMetaDescription = document.querySelector('meta[name="description"]');
const legalPage = document.body.dataset.legalPage;

function getLegalStoredLanguage() {
  try {
    const storedLanguage = window.localStorage.getItem("wms-language");
    return legalTranslations[storedLanguage] ? storedLanguage : null;
  } catch {
    return null;
  }
}

let legalCurrentLanguage = getLegalStoredLanguage() || "es";

function legalT(key) {
  return (
    legalTranslations[legalCurrentLanguage]?.[key] ??
    legalTranslations.es[key] ??
    ""
  );
}

function applyLegalLanguage(language) {
  legalCurrentLanguage = legalTranslations[language] ? language : "es";

  document.documentElement.lang = legalCurrentLanguage;

  const titleKey =
    legalPage === "privacy-policy" ? "privacyMetaTitle" : "legalNoticeMetaTitle";
  const descriptionKey =
    legalPage === "privacy-policy"
      ? "privacyMetaDescription"
      : "legalNoticeMetaDescription";

  document.title = legalT(titleKey);

  if (legalMetaDescription) {
    legalMetaDescription.setAttribute("content", legalT(descriptionKey));
  }

  legalTextNodes.forEach((node) => {
    node.textContent = legalT(node.dataset.i18n);
  });

  legalHtmlNodes.forEach((node) => {
    node.innerHTML = legalT(node.dataset.i18nHtml);
  });

  legalAriaNodes.forEach((node) => {
    node.setAttribute("aria-label", legalT(node.dataset.i18nAriaLabel));
  });

  legalLanguageButtons.forEach((button) => {
    button.classList.toggle(
      "is-active",
      button.dataset.language === legalCurrentLanguage
    );
  });

  try {
    window.localStorage.setItem("wms-language", legalCurrentLanguage);
  } catch {
    // Ignore storage issues and keep the current page language only.
  }
}

legalLanguageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLegalLanguage(button.dataset.language);
  });
});

applyLegalLanguage(legalCurrentLanguage);
